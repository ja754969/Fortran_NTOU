! CH06_0401_2
IMPLICIT NONE
REAL TOTAL,REDUCT,NET,TAX
INTEGER GRD
WRITE(*,*) 'input total income >> '
READ(*,*) TOTAL
TAX = 0.0
GRD = 0
REDUCT = TOTAL*0.15 !必要開銷
IF (REDUCT .GT. 60000.0) REDUCT=60000.0 ! 簡單條件敘述
NET = TOTAL-60000.0-REDUCT
WRITE(*,*) 'NET=',NET
IF(REDUCT .GT. 500000.0) then
    TAX=NET*0.1-14000.0
    GRD=3
ELSE IF (NET .GT. 200000.0) then
    TAX=NET*0.08-4000.0
    GRD=2
ELSE IF (NET .GT. 0.0) then
    TAX=NET*0.06
    GRD=1
END IF
WRITE(*,100) GRD,TAX
100 FORMAT('tax grid = ',I3,' TAX = ',F12.2)
END
