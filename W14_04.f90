IMPLICIT NONE
INTEGER N,I,J,A(3,3),K
REAL TOTAL1,AVG1,TOTAL2,TOTAL3
OPEN(20,FILE='FT_file_W14.dat')
READ(20,*) N
DO I = 1,N
    READ(20,*) (A(I,J),J=1,3)
    WRITE(*,110) ('A(',I,',',J,')=',A(I,J),J=1,3)
END DO
110 FORMAT(3(A4,I3,A2,I3,A4,I8))
TOTAL1 = 0.0;TOTAL2=0.0;AVG1=0.0;K=0
! ROW 的總和 : TOTAL1
DO I = 1,3
    TOTAL2 = 0.0
    DO J = 1,3
        TOTAL1 = TOTAL1+A(I,J)
        K = K+1
        TOTAL2 = TOTAL2+A(I,J)
    END DO
    WRITE(*,210) I,TOTAL2
END DO
AVG1 = TOTAL1/FLOAT(K)
WRITE(*,200) K,TOTAL1,AVG1
200 FORMAT('TOTAL num=',I3,' TOTAL1=',F10.2,' AVG1=',F10.2)
210 FORMAT(I3,F10.2)
! COLUMN 的總和 : TOTAL2
DO J = 1,3
    TOTAL3 = 0.0
    DO I = 1,3
        TOTAL3 = TOTAL3+A(I,J)
    END DO
    WRITE(*,210) J,TOTAL3
END DO
END
